    private void BtnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnConsultarActionPerformed
        String numerodoc = txtDocumento.getText().trim();
        int idtipoDocumento = comboDocumento.getSelectedIndex();
        int idEstado = comboEstado.getSelectedIndex();

        if(idtipoDocumento > 0){
            if(numerodoc != null && numerodoc.length()>0){

                usuariosDTO = fachada.consultarPersona(numerodoc);
                if(usuariosDTO != null){

                    txtNombre1.setText(usuariosDTO.getNombre1());
                    txtNombre2.setText(usuariosDTO.getNombre2());
                    txtApellido1.setText(usuariosDTO.getApellido1());
                    txtApellido2.setText(usuariosDTO.getApellido2());

                }else{
                    JOptionPane.showMessageDialog(this, "No se encontro el usuario!", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(this, "Campo documento vacio!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(this, "por favor seleccione el tipo de documento!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_BtnConsultarActionPerformed

