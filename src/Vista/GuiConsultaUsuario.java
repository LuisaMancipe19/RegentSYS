    private void cargarTipoDocumento() {
        listaTipoDocumento = fachada.consultarlistaTipoDocumento();
        for (DocumentoDTO DocumentoDTO : listaTipoDocumento) {
            comboDocumento.addItem(DocumentoDTO.getdescripcionDocumento());
        }
    }
    private void cargarListaEstado(){
        listaEstado = fachada.consultarlistaEstado();
        for (EstadoDTO EstadoDTO : listaEstado){
            comboEstado.addItem(EstadoDTO.getDescripcionEstado());
        }
    }

