    private void jGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jGuardarActionPerformed
        String numerodoc = txtDocumento.getText().trim();
        String nombre1 = txtNombre1.getText().trim();
        String nombre2 = txtNombre2.getText().trim();
        String apellido1 = txtApellido1.getText().trim();
        String apellido2 = txtApellido2.getText().trim();
        
        int idTipoDocumento = comboDocumento.getSelectedIndex();
        int idEstado = comboEstado.getSelectedIndex();
        
        numerodoc = numerodoc.toUpperCase();
        nombre1 = nombre1.toUpperCase();
        nombre2 = nombre2.toUpperCase();
        apellido1 = apellido1.toUpperCase();
        apellido2 = apellido2.toUpperCase();
        
        if(idTipoDocumento > 0){
            if(numerodoc != null && numerodoc.length()>0){
                if(nombre1 != null && nombre1.length()>0){
                    if(apellido1 != null && apellido1.length()>0){
                        if(idEstado > 0){
                            if(Validaciones.esNumero(numerodoc)){
                                if(Validaciones.esTexto(nombre1)){
                                    if(Validaciones.esTexto(apellido1)){
                                        if(Validaciones.esTexto(apellido2)){
                                            if(Validaciones.esTexto(nombre2)){
                                                
                                                UsuariosDTO UsuariosDTO = new UsuariosDTO();
                                                
                                                UsuariosDTO.setNombre1(nombre1);
                                                UsuariosDTO.setNombre2(nombre2);
                                                UsuariosDTO.setApellido1(apellido1);
                                                UsuariosDTO.setApellido2(apellido2);
                                                UsuariosDTO.setDocumento(numerodoc);
                                                
                                                DocumentoDTO DocumentoDTO = listaTipoDocumento.get(idTipoDocumento-1);
                                                UsuariosDTO.setIdTipoDocumento(DocumentoDTO.getIdDocumento());
                                                
                                                EstadoDTO EstadoDTO = listaEstado.get(idEstado-1);
                                                UsuariosDTO.setIdEstado(EstadoDTO.getIdEstado());
                                                
                                                boolean resultado = fachada.actualizarPersona(UsuariosDTO);
                                                if(resultado){
                                                Mensaje.setText("Mensaje: Usuario actualizado correctamente!");
                                                System.out.println("Usuario actualizado correctamente!");
                                                
                                                txtDocumento.setText("");
                                                txtNombre1.setText("");
                                                txtNombre2.setText("");
                                                txtApellido1.setText("");
                                                txtApellido2.setText("");
                                                comboDocumento.setSelectedIndex(0);
                                                comboEstado.setSelectedIndex(0);
                                                
                                                }else{
                                                System.out.println("Error al actualizar datos");
                                                }
                                                
                                            }else{
                                                JOptionPane.showMessageDialog(this, "El segundo nombre es texto!" , "Alerta!", JOptionPane.ERROR_MESSAGE);
                                            }
                                        }else{
                                            JOptionPane.showMessageDialog(this, "El segundo apellido es texto!" , "Alerta!", JOptionPane.ERROR_MESSAGE);
                                        }
                                    }else{
                                        JOptionPane.showMessageDialog(this, "El primer apellido es texto!" , "Alerta!", JOptionPane.ERROR_MESSAGE);
                                    }
                                }else{
                                    JOptionPane.showMessageDialog(this, "El primer nombre es texto!" , "Alerta!", JOptionPane.ERROR_MESSAGE);
                                }
                            }else{
                                JOptionPane.showMessageDialog(this, "El numero de documento debe ser numerico!" , "Alerta!", JOptionPane.ERROR_MESSAGE);
                            }
                        }else{
                            JOptionPane.showMessageDialog(this, "por favor seleccione el estado!", "Error", JOptionPane.ERROR_MESSAGE);
                        }
                    }else{
                        JOptionPane.showMessageDialog(this, "Campo primer apellido vacio!", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                }else{
                    JOptionPane.showMessageDialog(this, "Campo primer nombre vacio!", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(this, "Campo numero documento vacio!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }else{
           JOptionPane.showMessageDialog(this, "por favor seleccione el tipo de documento!", "Error", JOptionPane.ERROR_MESSAGE); 
        }
        
        
    }//GEN-LAST:event_jGuardarActionPerformed

